    public void loadGv() {
        dsUser.removeAllItems();
        ArrayList<String> ds = new ArrayList<>();
        ds = this.getDsGVAvailable();
        for (String s : ds) {
            dsUser.addItem(s);
        }
    }
    public void loadSv() {
        dsUser.removeAllItems();
        ArrayList<String> ds = new ArrayList<>();
        ds = this.getDssvAvailable();
        for (String s : ds) {
            dsUser.addItem(s);
        }
    }

    public int checkTrungLogin(String login) {
        int check = 0;
        try {
            CallableStatement pr = Main.kn.prepareCall("{? = CALL SP_TRUNG_LOGIN(?) }");
            pr.registerOutParameter(1, java.sql.Types.INTEGER);
            pr.setString(2, login);
            pr.execute();
            check = pr.getInt(1);
            if (check == 1) {

                return 1;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return 0;
    }

