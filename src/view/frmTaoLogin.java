    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // de trong
        String role ="";
        String loginn = loginame.getText();
        String password = String.valueOf(mk.getPassword());
        String xn = String.valueOf(xnmk.getPassword());
        String user = dsUser.getSelectedItem().toString();
        if (loginn.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống login");
        } else if (password.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống mật khẩu");
        } else if (xn.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống xác nhận");
        } else if (!xn.equals(password)) {
            JOptionPane.showMessageDialog(this, "Xác nhận mật khẩu không đúng");
        } else if (this.checkTrungLogin(loginn) == 1) {
            JOptionPane.showMessageDialog(this, "Tên login này đã được sử dụng");
        } 
        else {
            try {
                PreparedStatement pr = Main.kn.prepareCall("{CALL SP_ADDLOGIN(?,?,?)}"); // tao login
                pr.setString(1, loginn);
                pr.setString(2, password);
                pr.setString(3, "QLDSV_TC");
                pr.execute();
                pr = Main.kn.prepareCall("{CALL SP_GRANTDBACCESS( ?, ?)}"); // mapping user
                pr.setString(1, loginn);
                pr.setString(2, user);
                pr.execute();
                
                if(sv.isSelected()) {
                    role = "SV";
                }
                else if(khoa.isSelected()) {
                    role = "KHOA";
                }
                else if(kt.isSelected()) {
                    role = "KT";
                }
                else {
                    role ="PGV";
                   
                    pr = Main.kn.prepareCall("{CALL sp_addsrvrolemember(?,?)}");
                    pr.setString(1, loginn);
                    pr.setString(2, "securityadmin");
                    pr.execute();
                }
                // them login vao role 
                pr = Main.kn.prepareCall("{CALL sp_addrolemember(?,?)}");
                pr.setString(1, role);
                pr.setString(2, user);
                pr.execute();
                
                
                JOptionPane.showMessageDialog(this, "Thêm login thành công");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        sv.setSelected(true);
        this.loadSv();
        
        

        // check login ton tai

    }//GEN-LAST:event_jButton1ActionPerformed
    public ArrayList<String> getDsGVAvailable() {
        ArrayList<String> ds = new ArrayList<>();
        try {
            PreparedStatement pr = Main.kn.prepareCall(
                    "select MAGV from GIANGVIEN "
                    + "except "
                    + "select name from sys.sysusers where issqluser = 1 ");
            ResultSet rs = pr.executeQuery();
            while (rs.next()) {
                ds.add(rs.getString(1));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return ds;
    }
    public ArrayList<String> getDssvAvailable() {
        ArrayList<String> ds = new ArrayList<>();
        try {
            PreparedStatement pr = Main.kn.prepareCall(
                    "select MASV from SINHVIEN "
                    + "except "
                    + "select name from sys.sysusers where issqluser = 1");
            ResultSet rs = pr.executeQuery();
            while (rs.next()) {
                ds.add(rs.getString(1));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return ds;
    }
